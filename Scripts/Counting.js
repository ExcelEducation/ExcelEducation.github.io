function Question(question,answer,qnimage,qnlabel) {
	this.question = question;
	this.answer = answer;
	this.qnimage = qnimage;
	this.qnlabel = qnlabel;
};

function shuffle(o) {
	for(var j, x, i = o.length; i; j = parseInt(Math.random() * i), x = o[--i], o[i] = o[j], o[j] = x);
	return o;
};

function btnProvideQuestion(quiz) 
{ 		 
	if(currentQnNumber < numQuestions)
	{
		var qnNumber = qnNumbers[currentQnNumber]-1;
		randomQuestion = quiz[qnNumber]; //getQuestion
		answers = [randomQuestion.answer, randomQuestion.qnimage];
		shuffle(answers);

		document.getElementById("question").innerHTML= randomQuestion.question;
		if(document.getElementById("qnimage") != null)
			document.getElementById("qnimage").src = randomQuestion.qnimage; 
		else if(document.getElementById("sketchpad") != null)
			document.getElementById("sketchpad").style.background = "url('"+ randomQuestion.qnimage +"')";

		document.getElementById("inputlabel").innerHTML = randomQuestion.qnlabel; 
	}	
	
	currentQnNumber++;
}

function getRandQnNumbers()
{
	var arr = [];
	while(arr.length < numQuestions){
		var r = Math.floor(Math.random() * numPoolQuestions) + 1;
		if(arr.indexOf(r) === -1) arr.push(r);
	}
	
	return arr;
}

function nextClicked(quiz) {
	if(currentQnNumber <= numQuestions)
	{
		var answer = document.getElementById("userinput").value;
		checkAnswer(quiz, answer);		
		
		if(document.getElementById("sketchpad") != null)
			clearCanvas(canvas,ctx);
	}
}

function adjustScore(isCorrect) {
	if(currentQnNumber <= numQuestions)
	{
		debugger;
		if (isCorrect) {
			currentScore++;
		} else {
			if (currentScore > 0) {
				currentScore--;
			}
		}
		document.getElementById("score").innerHTML = currentScore;			
	}
}

